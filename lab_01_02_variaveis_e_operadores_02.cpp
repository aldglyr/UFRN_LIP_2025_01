/*
    Escreva um programa que:
    1. Atribui os valores 11, 19 e 23 a três variáveis do tipo inteiro;
    2.Imprime a soma e o produto dessas três variáveis;
*/

#include <iostream>

using namespace std;

int main(){
    int valor1 = 11;
    int valor2 = 19;
    int valor3 = 23;

    cout << valor1 + valor2 + valor3 << endl;
    cout << valor1 * valor2 * valor3 << endl;
    
    return 0;
}